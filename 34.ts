// Store pizza names in an array
let pizzas: string[] = ['Pepperoni', 'Margherita', 'Supreme'];

// Loop through the array and print the name of each pizza
for (let pizza of pizzas) {
    console.log(pizza);
}
// Loop through the array and print a sentence using the name of each pizza
for (let pizza of pizzas) {
    console.log(`I like ${pizza} pizza.`);
}
// Print a line stating how much you like pizza
console.log("I really love pizza!");
